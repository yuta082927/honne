入力された悩みの「表面」ではなく
その裏にある「本当の怖れ・痛み」を
情景・身体感覚で描写する。

BAD: 「恋人を求めているようです」
GOOD: 「いつできるか——本当に知りたいのはそこじゃない。
自分はちゃんと愛される人間なのか。
それが怖くて、期限を求めることで
答えを先送りにしている。」

